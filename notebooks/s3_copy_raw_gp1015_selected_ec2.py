# Dump Fn from S3
def s3_items_from_table(bucket, prefix):
    paginator = s3.get_paginator('list_objects_v2')
    pages = paginator.paginate(Bucket=bucket, Prefix=prefix)
    objects = []
    for page in pages:
        for item in page['Contents']:
            objects.append(item['Key'])
    
    return objects


